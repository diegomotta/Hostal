    private void btn_Capturar_ReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Capturar_ReservaActionPerformed
        Calendar hoy = Calendar.getInstance();
        Calendar diaReserva = Calendar.getInstance();

        List<Reserva_Habitacion> Lista_del_dia = new LinkedList<>();
        for (Reserva_Habitacion unaReserva_Habitacion : unaHosteria.getReservas()) {
            diaReserva.setTime(unaReserva_Habitacion.getDesdeFecha());
            if (diaReserva.get(Calendar.DAY_OF_YEAR) == hoy.get(Calendar.DAY_OF_YEAR)) {
                if (String.valueOf(unaReserva_Habitacion.getDeHabitacion().getNumero()).equals(txt_1.getText())) {
                    Lista_del_dia.add(unaReserva_Habitacion);
                }
            }
        }

        if (!Lista_del_dia.isEmpty()) {
            JDialog frame;
            if (SwingUtilities.getWindowAncestor(this) instanceof JDialog) {
                frame = new JDialog((JDialog) SwingUtilities.getWindowAncestor(this), true);
            } else {
                frame = new JDialog((JFrame) SwingUtilities.getWindowAncestor(this), true);
            }
            Capturar_RH unaL = new Capturar_RH(Lista_del_dia);
            frame.getContentPane().removeAll();
            frame.getContentPane().add(unaL);
            frame.pack();
            frame.setLocationRelativeTo(null);
            frame.show();

            if (unaL.getUnaReserva_Habitacion() != null) {
                seleccionada = unaL.getUnaReserva_Habitacion();
                dp_diaIN.setDate(seleccionada.getDesdeFecha());
                dp_diaOUT.setDate(seleccionada.getHastaFecha());
                cbx_Tarifas_Hab.setSelectedItem(seleccionada.getTipo());
                txt_Precio_Unitario.setText(String.valueOf(seleccionada.getTipo().getPrecio()));
                txt_Total.setText(String.valueOf(seleccionada.getTipo().getPrecio() * seleccionada.getCantidad_de_Noches()));
                txt_apellido_rep.setText(seleccionada.getApeVis());
                txt_nombre_rep.setText(seleccionada.getNombVis());
            }
        } else {
            JOptionPane.showMessageDialog(this, "No existen reservas de esta Habitación para el dia de la fecha");
        }
    }//GEN-LAST:event_btn_Capturar_ReservaActionPerformed

