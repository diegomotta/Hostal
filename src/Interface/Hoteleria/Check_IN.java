    public Check_IN(Hosteria unaHosteria, JButton elBoton) {
        this.unaHosteria = unaHosteria;
        this.elBoton = elBoton;
        initComponents();
        GUIM.decimalPositivo(txt_Descuento);
        timer.start();
        if (elBoton != null) {
            txt_1.setText(elBoton.getName());
        }
        Calendar calendario = Calendar.getInstance();
        dp_diaIN.setDate(calendario.getTime());
        calendario.add(Calendar.DAY_OF_MONTH, 1);
        dp_diaOUT.setDate(calendario.getTime());

        Integer habitacionBuscada = Integer.parseInt(elBoton.getName());
        if (GUIM.retornarHabitacion(habitacionBuscada, unaHosteria) != null) {
            Habitacion unaHabitacion = GUIM.retornarHabitacion(habitacionBuscada, unaHosteria);
        }
        for (Tarifa_Habitacion unTarifa : unaHosteria.getTarifas_Habitacion()) {
            cbx_Tarifas_Hab.addItem(unTarifa);
        }
        cbx_Tarifas_Hab.setSelectedIndex(0);
        txt_Precio_Unitario.setText(String.valueOf(((Tarifa_Habitacion) cbx_Tarifas_Hab.getSelectedItem()).getPrecio()));



        cbx_Tarifas_Hab.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent ie) {
                if (cbx_Tarifas_Hab.getSelectedIndex() != -1) {
                    Tarifa_Habitacion unaTarifa = (Tarifa_Habitacion) cbx_Tarifas_Hab.getSelectedItem();

                    switch (unaTarifa.getTipo()) {
                        case "Single":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));
                        case "Doble Matrimonial":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));
                        case "Doble Separada":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));
                        case "Matrimonial + 1":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));
                        case "Trible Separada":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));
                        case "Matrimonial + 2":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));
                        case "Cuadruble Separada":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));
                        case "Sin Cargo":
                            txt_Precio_Unitario.setText(String.valueOf(unaTarifa.getPrecio()));

                    }
                }
            }
        });

        txt_Descuento.addCaretListener(new CaretListener() {
            @Override
            public void caretUpdate(CaretEvent ce) {
                calcularDescuento();
            }
        });

    }
