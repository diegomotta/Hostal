    private void btn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AceptarActionPerformed
        Articulo unArticulo = (Articulo) cbx_articuloFaltante.getSelectedItem();
        Area unArea = unArticulo.getArea_aplicacion();
        double unMonto = Double.parseDouble(txt_valor.getText());
        String unaCausa = (String) jComboBox1.getSelectedItem();
        
        try {
            unaHosteria.registrarFaltante(unArticulo, unMonto, unaCausa);
            unaHosteria.crearAuditoria("ADD", "Agregó un nuevo faltante, (" + unArticulo.getNombre() + "), con causa asignada a " + unaCausa, Acceso.getUsuario(), GUIM.getHoraActual());
            SwingUtilities.getWindowAncestor(this).dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getCause());
        }
        
        
    }//GEN-LAST:event_btn_AceptarActionPerformed
    
