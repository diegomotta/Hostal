    private void cbx_articuloFaltanteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbx_articuloFaltanteItemStateChanged
        if (cbx_articuloFaltante.getSelectedItem() instanceof Articulo_Estatico) {
            txt_valor.setText(String.valueOf(((Articulo_Estatico) cbx_articuloFaltante.getSelectedItem()).getPrecio()));
        } else {
            txt_valor.setText("");
        }
    }//GEN-LAST:event_cbx_articuloFaltanteItemStateChanged
    
    private void buttonCircle2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCircle2ActionPerformed
        
        JDialog frame;
        if (SwingUtilities.getWindowAncestor(this) instanceof JDialog) {
            frame = new JDialog((JDialog) SwingUtilities.getWindowAncestor(this), true);
        } else {
            frame = new JDialog((JFrame) SwingUtilities.getWindowAncestor(this), true);
        }
        ABM_Artículos gestorArtículos = new ABM_Artículos(unaHosteria);
        frame.getContentPane().removeAll();
        frame.getContentPane().add(gestorArtículos);
        frame.pack();
        frame.setLocationRelativeTo(null);
        frame.show();
        
        cargarCompo();
    }//GEN-LAST:event_buttonCircle2ActionPerformed

    private void txt_valorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_valorKeyTyped
        if (!GUIM.esEntero(evt.getKeyChar())) {
            evt.consume();
        }
    }//GEN-LAST:event_txt_valorKeyTyped
    
    private void cargarCompo() {
        for (Iterator<Articulo> it = unaHosteria.getArticulos().iterator(); it.hasNext();) {
            Articulo unArticulo = it.next();
            cbx_articuloFaltante.addItem(unArticulo);
        }
        
    }
